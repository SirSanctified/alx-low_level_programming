#include "3-calc.h"

/**
 * op_add - addition
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: first number
 * @b: second number
 * Return: difference of a nd b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first number
 * @b: second number
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first number
 * @b: second number
 * Return: qoutient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 * @a: first number
 * @b: second number
 * Return: modulus of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}

